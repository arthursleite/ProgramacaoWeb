const n1 = 12;
const n2 = 9;
let resultado;

resultado = n1 + n2;
console.log("Resultado da soma entre " + n1 + " e " + n2 + " é: " + resultado);

resultado = n1 * n2;
console.log("Resultado da multiplicação entre", n1, "e", n2, "é:", resultado);

resultado = n1 - n2;
console.log(`Resultado da subtração entre ${n1} e ${n2} é: ${resultado}`);

resultado = n1 / n2;
console.log("Resultado da divisão entre", n1, "e", n2, "é:", resultado);